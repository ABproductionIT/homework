P = float(input("մուտքագրեք վարկի գումարի չափը ———>"))
tokoss = float(input("տարեկան տոկոսադրույքը % --->"))
i = float(tokoss)/100
t = 12   # ամսեկանի համար տարին=12ամիսա
T = int(input("ժամկետը ամիս---->"))
n = T / t
I = P * n * i
S = P + I
print("ընդհանուր վճարված գումաը ժամկետի ավարտին կկազմի", S)
