P = float(input("մուտքագրեք վարկի գումարի չափը ———>"))
tokos = float(input("տարեկան տոկոսադրույքը % --->"))
i = tokos/100
t = 12   # ամսեկանի համար տարին=12ամիսա
T = float(input("ժամկետը ամիս---->"))
n = T / t
I = P * n * i
S = P + I
print("ընդհանուր վճարված գումաը ժամկետի ավարտին կկազմի", S, ", որից", P, "-ը մայր գումար, իսկ", I, "տոկոսագումարը")
